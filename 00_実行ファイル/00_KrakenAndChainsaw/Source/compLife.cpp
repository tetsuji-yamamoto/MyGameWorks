//********************************************************
// 描画
//********************************************************
void CLife::AddLife(int nAddLife)
{
	// 値追加
	m_argument.nLife += nAddLife;

	// クランプ
	m_argument.nLife = Clamp(m_argument.nLife, m_argument.nMin, m_argument.nMax);
}

//********************************************************
// 体力がなくなったかの取得
//********************************************************
bool CLife::GetIsMin(void)
{
	return Min(m_argument.nLife, m_argument.nMin);
}

//********************************************************
// 体力がなくなったかの取得
//********************************************************
bool CLife::GetIsMax(void)
{
	return Max(m_argument.nLife, m_argument.nMax);
}